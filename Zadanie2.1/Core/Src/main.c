// Sends data to WS2812B
void SetLED(uint8_t green, uint8_t red, uint8_t blue)
{
	// encode rgb value to DMA buffer
	uint32_t data = (green << 16) | (red << 8) | blue;
	int dataIndex = 0;
	for (int i = 23; i >= 0; --i)
	{
		if(data & (1 << i)) ledDma[dataIndex] = 128; // 128 / 200 * 1.25us = 0.8us -> 1
		else ledDma[dataIndex] = 64; // 64 / 200 * 1.25us = 0.4us -> 0
		++dataIndex;
	}

	// transmission stop
	for (int i = 0; i < 50; ++i)
	{
		ledDma[dataIndex++] = 0;
	}

	HAL_TIM_PWM_Start_DMA(&htim1, TIM_CHANNEL_1, (uint32_t*)ledDma, 24+50);
	ledReadyForData = 0;
}

// Calculates LED color and sends data to WS2812B
void UpdateLED()
{
	if (!ledReadyForData) return;

	const uint32_t millis = HAL_GetTick();
	const uint32_t colorCycleInterval = 3000; // ms
	const uint32_t colorCycleIntervalHalf = colorCycleInterval / 2;
	static uint32_t lastButtonPressTime = INT32_MAX;
	const uint32_t timeSinceLastPress = millis - lastButtonPressTime;

	uint8_t red = ((millis / colorCycleIntervalHalf) % 2) * 255;
	uint8_t green = (millis % colorCycleInterval) * 255 / colorCycleInterval;
	uint8_t blue = 0;

	if (timeSinceLastPress >= colorCycleInterval * 3)
	{
		if (HAL_GPIO_ReadPin(PIN_BUTTON_GPIO_Port, PIN_BUTTON_Pin)) // button with pull down resistor
		{
			lastButtonPressTime = millis;
		}
	}
	else
	{
		blue = ((timeSinceLastPress % colorCycleInterval) < colorCycleIntervalHalf) * 255;
	}

	// Correct led brightness
	green = ledBrightnessCorrection8[green];
	//red = ledGammaCorrection8[red]; // not needed because it's only on or off
	//blue = ledGammaCorrection8[blue]; // not needed because it's only on or off

	SetLED(green, red, blue);
}
